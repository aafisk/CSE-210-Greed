class Position {

    protected int X = 0;
    protected int Y = 0;
    
    public Position() {

    }

    public int GetX() {
        return X;
    }

    public void SetX(int x) {
        X = x;
    }

    public int GetY() {
        return Y;
    }

    public void SetY(int y) {
        Y = y;
    }
}